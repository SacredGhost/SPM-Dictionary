from bighexlist import HexList
from watches import game_region, game_revision

class EnemyEntry():
    def __init__ (self, EnemyName, EnemyData, EnemyID, DropTemplate, DropPercent, EnemyDrops):
        self.EnemyName = EnemyName
        self.EnemyData = EnemyData
        self.EnemyID =  EnemyID
        self.DropTemplate = DropTemplate
        self.DropPercent = DropPercent
        self.EnemyDrops = EnemyDrops

class EnemyData():
    def __init__ (self, HP, Atk, Def, Tattle, CardData, icon):
        self.HP = HP
        self.Atk = Atk
        self.Def = Def
        self.Tattle = Tattle
        self.CardData = CardData
        self.icon = icon

class CardData():
    def __init__ (self, CardNum, Name, Description, Location, Rarity):
        self.CardNum = CardNum
        self.Name = Name
        self.Descrption = Description
        self.Location = Location
        self.Rarity = Rarity

class ItemDrops():
    def __init__(self, *item):
        self.item = list(item)

    def add_item(self, item):
        self.item.append(item)

    def remove_item(self, item):
        self.item.remove(item)

    def get_item(self, index):
        if 0 <= index < len(self.item):
            return self.item[index]
        else:
            return None
        
class EnemyDrops():
    def __init__(self, *item):
        self.item = list(item)

    def add_item(self, item):
        self.item.append(item)

    def remove_item(self, item):
        self.item.remove(item)

    def get_item(self, index):
        if 0 <= index < len(self.item):
            return self.item[index]
        else:
            return None
        
class Drop():
    def __init__(self, ItemName, ItemID, ItemWeight):
        self.ItemName = ItemName
        self.ItemID = ItemID
        self.ItemWeight = ItemWeight

def get_itemPercent(name: str) -> int:
    drop = EnemyList[name]
    dropies = drop["Drop %"][game_region][game_revision]
    return int(dropies, drop["datatype"])

